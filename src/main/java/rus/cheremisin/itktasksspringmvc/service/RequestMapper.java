package rus.cheremisin.itktasksspringmvc.service;

import org.springframework.web.multipart.MultipartFile;

/**
 * Обобщённый интерфейс для преобразования данных между
 * файловым представлением (например, JSON) и сущностями приложения.
 *
 * <p>Выступает в роли адаптера между HTTP-слоем (MultipartFile)
 * и доменной моделью, инкапсулируя логику сериализации
 * и десериализации данных.</p>
 *
 * <p>Позволяет:
 * <ul>
 *     <li>Преобразовывать входящие файлы в объекты (десериализация)</li>
 *     <li>Формировать JSON-представление объектов для отдачи клиенту (сериализация)</li>
 * </ul>
 * </p>
 *
 * <p>Используется в контроллерах для обработки multipart-запросов
 * и формирования файловых ответов.</p>
 *
 * <p>Обобщённый тип {@code <T>} позволяет использовать единый контракт
 * для различных сущностей (Product, Customer, Order и т.д.).</p>
 *
 * <p>В приложении может существовать несколько реализаций данного интерфейса,
 * поэтому при внедрении зависимостей может потребоваться использование
 * {@link org.springframework.beans.factory.annotation.Qualifier}.</p>
 *
 * @param <T> тип сущности, с которой работает маппер
 */
public interface RequestMapper<T> {
    /**
     * Преобразует входящий файл в объект заданного типа.
     *
     * <p>Ожидается, что содержимое файла представлено в формате JSON.
     * В случае ошибки парсинга выбрасывается исключение.</p>
     *
     * @param file multipart-файл с JSON-данными
     * @return десериализованный объект
     * @throws rus.cheremisin.itktasksspringmvc.exception.JsonToObjectMappingException если произошла ошибка при чтении или преобразовании данных
     */
    T toEntity(MultipartFile file);

    /**
     * Сериализует объект в JSON и возвращает его в виде массива байт.
     *
     * <p>Используется, например, для формирования файлового ответа
     * в REST API.</p>
     *
     * @param t объект для сериализации
     * @return JSON-представление объекта в виде массива байт
     * @throws rus.cheremisin.itktasksspringmvc.exception.ObjectToJsonMappingException если произошла ошибка при сериализации
     */
    byte[] toJsonFile(T t);

}
